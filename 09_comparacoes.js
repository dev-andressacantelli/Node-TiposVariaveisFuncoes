// igualdade estrita (ou "três iguais" ou "identidade") usando === compara valor e tipo
// igualdade ampla ("dois iguais") usando == compara apenas o valor

const numero = 5;
const texto = '5';

console.log(numero == texto)
console.log(numero === texto)

//Usando typeof para entender as comparações:
console.log(typeof numero)
console.log(typeof texto)

